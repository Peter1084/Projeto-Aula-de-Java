           private void habilitarCampos() {
        jtfNome.setEnabled(true);
        jtfCodigo.setEnabled(false);
    }

    private void desabilitarCampos() {
        jtfNome.setEnabled(false);
        jtfCodigo.setEnabled(true);
    }

    private void enviarForm() {
        if (this.jtfCodigo.getText().equals("")) {
            this.controller.getMarcaManipulado().setCodigo(null);
        } else {
            this.controller.getMarcaManipulado().setCodigo(Integer.parseInt(this.jtfCodigo.getText()));
        }
        this.controller.getMarcaManipulado()
                .setNome(this.jtfNome.getText());
        
        } 
    

    private void LimparCampos() {
        jtfNome.setText("");
        jtfCodigo.setText("");
        }

    private void recebeForm() {
       jtfCodigo.setText(this.controller.getMarcaManipulado().getCodigo().toString());
       jtfNome.setText(this.controller.getMarcaManipulado().getNome());
       } 

    private void validarForm() throws Exception {
        if (jtfNome.getText().equals("")) {
             throw new Exception("O Campo Nome nÃ£o pode ser vazio!");
         }
         
           
    }

